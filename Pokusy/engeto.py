# Vypsané texty s upravenými stringy
# Výpis prvních pěti znaků
print("Prvních 5 písmen:")
print("indexování"[:5])

# Výpis poslední pěti znaků
print("Posledních 5 písmen:")
print("indexování"[-5:])

# Výpis každého třetího znaku počínaje 'i'
print("Každé 3. písmeno (počínaje prvním) od 'i':")
print("indexování"[::3])
