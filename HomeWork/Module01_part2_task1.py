# Vstup čísel
cislo1 = float(input("Zadej 1. číslo:"))
cislo2 = float(input("Zadej 2. číslo:"))
cislo3 = float(input("Zadej 3. číslo:"))

# Součet čísel
soucet = cislo1 + cislo2 + cislo3
print(f"Součet čísel {cislo1}, {cislo2} a {cislo3} se rovná: {soucet}")

# Součin čísel
soucin = cislo1 * cislo2 * cislo3
print(f"Součin čísel {cislo1}, {cislo2} a {cislo3} se rovná: {soucin}")